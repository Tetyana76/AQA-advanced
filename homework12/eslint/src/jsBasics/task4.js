// Обчисліть площу кола за формулою π * radius^2
let radius1 = 5.2;
let square1 = (Math.PI * Math.pow(radius1, 2)).toFixed(2);
console.log('Площа кола:', square1);

// Обчисліть площу прямокутника за формулою length * width
let length = 3.5;
let width = 1.8;
let square2 = (length * width).toFixed(2);
console.log('Площа прямокутника:', square2);

// Обчисліть об'єм циліндра за формулою π * radius^2 * height
let radius2 = 7;
let height = 12;
let square3 = (Math.PI * Math.pow(radius2, 2) * height).toFixed(2);
console.log('Площа циліндра:', square3);
